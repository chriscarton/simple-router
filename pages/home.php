<p>
    <i>Simple router</i> est de loin la façon la plus simple de construire de petits sites statiques en <strong>PHP</strong> comportant néanmoins quelques pages !
</p>

<p>
    Ce système fonctionne en local avec un environnement de développement comme <i>Laragon</i>. Il nécessite un <i>.htaccess</i> pour la réecriture d'url.
</p>

<p>
    J'espère que vous apprécierez ce petit système. Aussi si vous êtes développeur n'hésitez pas à l'améliorer et à le réutiliser dans vos projets. 
</p>